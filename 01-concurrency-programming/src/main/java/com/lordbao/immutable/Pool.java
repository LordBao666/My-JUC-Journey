@Slf4j
public class Pool {
    private final int poolSize;
    private final Connection[] connections;

    //记录各个连接的状态
    //0 表示空闲 1表示繁忙
    private final AtomicIntegerArray states;

    public Pool(int poolSize) {
        if(poolSize<=0){
            log.debug("pool size must be at least 1");
            throw new IllegalArgumentException("pool size must be at least 1");
        }
        this.poolSize = poolSize;
        connections = new Connection[poolSize];
        states =  new AtomicIntegerArray(poolSize);

        for (int i = 0; i < poolSize; i++) {
            connections[i]=new MockConnection("Connection@"+i);
        }
    }


    public Connection borrow(){
        while (true){
            //这里只是用简单的便利来确定是否空闲
            for (int i = 0; i < poolSize; i++) {
                if(states.get(i)==0){//空闲
                    if(states.compareAndSet(i,0,1)){
                        log.debug("{} is borrowed",connections[i]);
                        return connections[i];
                    }
                }
            }

            //等待
            synchronized (this){
                try {
                    this.wait();
                } catch (InterruptedException e) {
                    throw new RuntimeException(e);
                }
            }
        }

    }

    public void free(Connection conn){
        for (int i = 0; i < poolSize; i++) {
            if(connections[i]==conn){
                states.set(i,0);//这里可以用set
                log.debug("{} is freed ", connections[i]);
                synchronized (this){
                    this.notifyAll();//唤醒等待的线程
                }
                break;
            }
        }
    }
}

